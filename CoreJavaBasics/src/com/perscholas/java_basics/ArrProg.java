package com.perscholas.java_basics;

public class ArrProg {
	
	private int intArr[];
	
	public ArrProg() {
		intArr = new int[3];
		intArr[0] = 1;
		intArr[1] = 2;
		intArr[2] = 3;
		System.out.println("IntArr[0] is: " + intArr[0]);
		System.out.println("IntArr[1] is: " + intArr[1]);
		System.out.println("IntArr[2] is: " + intArr[2]);
	}
}
